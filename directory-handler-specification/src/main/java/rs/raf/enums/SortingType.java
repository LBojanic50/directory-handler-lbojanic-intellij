package rs.raf.enums;

/**
 * File list sorting types.
 */
public enum SortingType {
    /**
     * None sorting type.
     */
    NONE,
    /**
     * Name sorting type.
     */
    NAME,
    /**
     * Date created sorting type.
     */
    DATE_CREATED,
    /**
     * Date modified sorting type.
     */
    DATE_MODIFIED,
    /**
     * Size sorting type.
     */
    SIZE
}